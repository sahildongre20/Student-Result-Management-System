    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollnumber = rollText.getText();
                
        try{
//            driver class of sql
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","admin");
            Statement st = con.createStatement();
            
            ResultSet rs =st.executeQuery("select * from student inner join result where student.rollnumber='"+rollnumber+"' and  result.rollnumber='"+rollnumber+"' ");
            
            if(rs.next()){
                nameText.setText(rs.getString(4));
                courseText.setText(rs.getString(2));
                branchText.setText(rs.getString(3));
                yearText.setText(rs.getString(7));
                genderText.setText(rs.getString(6));
                mathText.setText(rs.getString(9));
                phyText.setText(rs.getString(10));
                chemText.setText(rs.getString(11));
                cprogText1.setText(rs.getString(12));
                
                int math = Integer.parseInt(mathText.getText());
                int phy = Integer.parseInt(phyText.getText());
                int chem = Integer.parseInt(chemText.getText());
                int cprog = Integer.parseInt(cprogText1.getText());
                
                int total = math+phy+chem+cprog;
                totaltext.setText(String.valueOf(total));

                
                



               
            }
            
           
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
            
        }
    }//GEN-LAST:event_formComponentShown

