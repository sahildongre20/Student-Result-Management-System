    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        String rollnumber = rollnumberText.getText();
        String course =(String)coursebox.getSelectedItem();
        String branch =(String)branchbox.getSelectedItem();
        String name = nameText.getText();
        String fatherName = fatherNameText.getText();
        String gender = (String) genderbox.getSelectedItem();
        String year = (String) yearbox.getSelectedItem();
        
        System.out.println(rollnumber+" "+course+" "+branch+" "+name+" "+fatherName+" "+gender+" "+year );
        try{
//            driver class of sql
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","admin");
            
            Statement st = con.createStatement();
            st.executeUpdate("insert into student(rollnumber, course,branch,name,fatherName,gender,year) values('"+rollnumber+"','"+course+"','"+branch+"','"+name+"','"+fatherName+"','"+gender+"','"+year+"')");
                JOptionPane.showMessageDialog(null, "Data Saved Succesfully");
            setVisible(false);
            new adminHome().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
            
        }

    }//GEN-LAST:event_saveBtnActionPerformed

