    private void saveResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveResultActionPerformed
        // TODO add your handling code here:
        String rollnumber = rollnumberText.getText();
        String maths =  mathsText.getText();
        String physics =  physicsText.getText();
        String chemistry = chemText.getText();
        String cprog = cprogText.getText();
        
         try{
//            driver class of sql
            Class.forName("com.mysql.cj.jdbc.Driver");
//            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","admin");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","admin");
            Statement st = con.createStatement();
            ResultSet rs =st.executeQuery("select * from student where rollnumber='"+rollnumber+"' ");
            
            if(rs.next()){
                st.executeUpdate("insert into result(rollnumber,maths,physics,chemistry,cprog) values('"+rollnumber+"','"+maths+"','"+physics+"','"+chemistry+"','"+cprog+"')");
                JOptionPane.showMessageDialog(null, "Result added Succesfully");
                setVisible(false);
                new addResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Student Not Found , Add Student To Databse Using Add Student");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
            
        }
        

        
    }//GEN-LAST:event_saveResultActionPerformed

